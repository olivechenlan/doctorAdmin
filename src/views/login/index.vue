<template>
  <div class="login-container">
    <el-form ref="dataForm" :model="loginForm" :rules="rules" class="login-form" auto-complete="on">
      <div class="title-container">
        <h3 class="title">医生端后台管理平台</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.phone"
          placeholder="请输入用户名"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.pwd"
          :type="passwordType"
          placeholder="请输入密码"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">登录</el-button>

    </el-form>
  </div>
</template>

<script>

export default {
  data() {
    return {
      loginForm: {
        phone: '',
        pwd: '',
        macType: '3',
        loginType: '1'
      },
      rules: {
        phone: [{ required: true, trigger: 'blur', message: '请输入用户名' }],
        pwd: [{ required: true, trigger: 'blur', message: '请输入密码' }]
      },
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.dataForm.validate(valid => {
        if (valid) {
          this.$loading().show()
          this.api.doctorApi.login(this.loginForm).then(data => {
            this.$loading().hide()
            if (data.responseFlag === '1') {
              this.$store.dispatch('user/toggleUserInfo', Object.assign({}, data.data, { refreshTime: this.dayjs() }))
              this.$router.replace({ path: this.redirect })
            } else {
              this.$message.error(data.responseMessage)
            }
          }).catch((err) => {
            this.$loading().hide()
          })
        }
      })
    }
  }
}
</script>

